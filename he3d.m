function dy=he3d(t,y,w,fm)

dy=zeros(12,1);
r1=sqrt(y(1).^2+y(2).^2+y(3).^2);
r2=sqrt(y(7).^2+y(8).^2+y(9).^2);
r12=sqrt((y(1)-y(7)).^2+(y(2)-y(8)).^2+(y(3)-y(9)).^2);
Z=2;
dy(1)=y(4);
dy(2)=y(5);
dy(3)=y(6);
dy(4)=-Z*y(1)./r1.^3+(y(1)-y(7))./r12^3;
dy(5)=-Z*y(2)./r1.^3+(y(2)-y(8))./r12^3;
dy(6)=-Z*y(3)./r1.^3+(y(3)-y(9))./r12^3-laser(t,w,fm);

dy(7)=y(10);
dy(8)=y(11);
dy(9)=y(12);
dy(10)=-Z*y(7)./r2.^3-(y(1)-y(7))./r12^3;
dy(11)=-Z*y(8)./r2.^3-(y(2)-y(8))./r12^3;
dy(12)=-Z*y(9)./r2.^3-(y(3)-y(9))./r12^3-laser(t,w,fm);
